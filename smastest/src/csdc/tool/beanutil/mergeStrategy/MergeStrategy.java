package csdc.tool.beanutil.mergeStrategy;

/**
 * 合并策略
 * @author xuhan
 *
 */
public interface MergeStrategy<T> {
	
	/**
	 * value1是原有的、待被覆盖的一方<br>
	 * value2是外来的一方<br>
	 * 合并结果将替换掉value1
	 * @param value1
	 * @param value2
	 * @return
	 */
	public T merge(T value1, T value2);

}
